    public ArrayList<Trainer> loadTrainers(){
        try (ObjectInputStream ois = new ObjectInputStream(new FileInputStream("trainer.ser"))) {
            return (ArrayList<Trainer>) ois.readObject();
        } catch (IOException | ClassNotFoundException e) {
            return new ArrayList<>();
        }
    }
